#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
		write(1, str++, 1);
	write(1, "\n", 1);
}

int	main(int argc, char *argv[])
{
	int	num;

	if (argc < 2)
		return (1);
	num = 1;
	while (num < argc)
		ft_putstr(argv[num++]);
	return (0);
}
